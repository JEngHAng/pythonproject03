name = input("ใส่ชื่อของตุณ : ")
year = int(input("กรุณาใส่ปีเกิด : "))
old = int(2023) - year
print ("-------------------------------------------")
print (f'ยินดีต้อนรับ : {name} เกิดปี : {year} อายุ : {old}')
print ("-------------------------------------------")
print ("ยินดีต้อนรับ :",name,"เกิดปี :",year,"อายุ :",old)
print ("-------------------------------------------")
print ("ยินดีต้อนรับ : "+name+" เกิดปี : "+str(year)+" อายุ : "+str(old))
print ("-------------------------------------------")
print ("ยินดีต้อนรับ : {} เกิดปี : {} อายุ : {}".format(name,year,old))
print ("-------------------------------------------")
print ("ยินดีต้อนรับ : %s เกิดปี : %s อายุ : %d"%(name,year,old))
print ("-------------------------------------------")
print ("ยินดีต้อนรับ :",name)
print ("ปีที่เกิดของคุณ :",year)
